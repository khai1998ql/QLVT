    private void loadChiNhanh() {
        txtMaNv.setText(Integer.toString(nvEdit.getMaNV()));
        txtHo.setText(nvEdit.getHo());
        txtTen.setText(nvEdit.getTen());
        txtDiachi.setText(nvEdit.getDiachi());
        txtNgaysinh.setText(DateUtil.convertDate(nvEdit.getNgaysinh()));
        txtLuong.setText(Float.toString(nvEdit.getLuong()));     
        cbMaCn.setSelectedItem(nvEdit.getChinhanh().getMaCN());
    }
    private void loadCbChiNhanh() {
        cbMaCn.removeAllItems();
        List<ChiNhanh> list = chiNhanhDao.findAll();
        for (ChiNhanh k : list) {
            cbMaCn.addItem(k.getMaCN());
        }
    }

