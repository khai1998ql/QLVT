    private void btnLuuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLuuActionPerformed
        // TODO add your handling code here:
        int nvMa=nvEdit.getMaNV();
        nvEdit.setMaNV(Integer.parseInt(txtMaNv.getText()));
        nvEdit.setHo(txtHo.getText());
        nvEdit.setTen(txtTen.getText());
        nvEdit.setDiachi(txtDiachi.getText());
        nvEdit.setNgaysinh(DateUtil.convertDate(txtNgaysinh.getText()));
        nvEdit.setLuong(Float.parseFloat(txtLuong.getText()));
        nvEdit.setGhichu(txtGhichu.getText());
        nvEdit.setChinhanh(new ChiNhanh(cbMaCn.getSelectedItem().toString(),null,null,null));
        nvEdit = nvDao.update(nvEdit,nvMa);
        if (nvEdit == null) {
            JOptionPane.showMessageDialog(rootPane, "Thất bại !");
        } else {
            JOptionPane.showMessageDialog(rootPane, "Thành công !");
            qlnv.loadTable(nvDao.findAll());
            this.dispose();
        }
    }//GEN-LAST:event_btnLuuActionPerformed

