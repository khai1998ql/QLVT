    private void btUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btUpdateActionPerformed
        // TODO add your handling code here:
        try {
            String id = tableModel.getValueAt(tableKho.getSelectedRow(), 0).toString();
            khoEdit = khoDao.findByMaKho(id);
            SuaKho suaKho = new SuaKho(this, rootPaneCheckingEnabled);
            suaKho.setVisible(true);
            suaKho.setLocationRelativeTo(null);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(rootPane, "Chưa chọn kho ?");
        }

    }//GEN-LAST:event_btUpdateActionPerformed

    private void btDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btDeleteActionPerformed
        // TODO add your handling code here:
        try {
            String id = tableModel.getValueAt(tableKho.getSelectedRow(), 0).toString();
            int ok = JOptionPane.showConfirmDialog(rootPane, "Xóa Kho ?");
            if (ok == JOptionPane.YES_OPTION) {
                boolean r = khoDao.delete(id);
                if (!r) {
                    JOptionPane.showMessageDialog(rootPane, "Thất bại !");
                } else {
                    loadTable(khoDao.findAll());
                }
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(rootPane, "Chưa chọn kho");
        }
    }//GEN-LAST:event_btDeleteActionPerformed

