    private void initCbChiNhanh() {
        List<ChiNhanh> list = chiNhanhDao.findAll();
        for (ChiNhanh k : list) {
            cbSearchCn.addItem(k.getMaCN());
        }
    
    }
    public void loadTable(List<Kho> list) {
        tableModel.setRowCount(0 );
        for (Kho kho : list) {
            tableModel.addRow(new Object[]{
                kho.getMaKho(),
                kho.getTenKho(),
                kho.getDiacchi(),
                kho.getChinhanh().getMaCN()
            });
        }
    }
    private String getCbNChiNhanh() {
        if ("Chi Nhánh".equals(cbSearchCn.getSelectedItem().toString())) {
            return null;
        }
        return cbSearchCn.getSelectedItem().toString();
    }

