    private void initCbChiNhanh() {
        List<ChiNhanh> list = chiNhanhDao.findAll();
        for (ChiNhanh k : list) {
            cbSearchCn.addItem(k.getMaCN());
        }
    
    }
    public void loadTable(List<NhanVien> list) {
        tableModel.setRowCount(0 );
        for (NhanVien nv : list) {
            tableModel.addRow(new Object[]{
                nv.getMaNV(),
                nv.getHo(),
                nv.getTen(),
                nv.getDiachi(),
                nv.getNgaysinh(),
                nv.getLuong(),
                nv.getGhichu(),
                nv.getChinhanh().getMaCN()
            });
        }
    }
    private String getCbNChiNhanh() {
        if ("Chi Nhánh".equals(cbSearchCn.getSelectedItem().toString())) {
            return null;
        }
        return cbSearchCn.getSelectedItem().toString();
    }

