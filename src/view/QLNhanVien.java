    private void btnEditNvActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditNvActionPerformed
        // TODO add your handling code here:
        try {
            String id = tableModel.getValueAt(tableNv.getSelectedRow(), 0).toString();
            int ok = JOptionPane.showConfirmDialog(rootPane, "Xóa nhân viên ?");
            if (ok == JOptionPane.YES_OPTION) {
                boolean r = nvDao.delete(id);
                if (!r) {
                    JOptionPane.showMessageDialog(rootPane, "Thất bại !");
                } else {
                    loadTable(nvDao.findAll());
                }
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(rootPane, "Chưa chọn nhân viên ?");
        }
    }//GEN-LAST:event_btnEditNvActionPerformed

